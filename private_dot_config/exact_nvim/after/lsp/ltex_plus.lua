local function fasttext_binary_path()
  local sysname = vim.uv.os_uname().sysname
  if sysname == "Windows_NT" then
    -- return vim.fn.expand("~/AppData/Local/nvim/state/tabnine.log")
    vim.notify("Fasttext binary path not configured fully for this platform")
    return "fasttext"
  elseif vim.g.is_macos then
    return "/usr/local/bin/fasttext"
  else
    vim.notify("Fasttext binary path not configured fully for this platform")
    return vim.fn.expand("~/.local/state/nvim/tabnine.log")
  end
end

local function java_path()
  local java_home = os.getenv("JAVA_HOME") or ""
  if java_home ~= "" then
    return java_home
  end

  local sysname = vim.uv.os_uname().sysname
  if sysname == "Windows_NT" then
    -- return vim.fn.expand("~/AppData/Local/nvim/state/tabnine.log")
    vim.notify("Java binary path not configured fully for this platform")
    return nil
  elseif vim.g.is_macos then
    return "/usr/local/Cellar/openjdk@17/17.0.16/libexec/openjdk.jdk/Contents/Home"
  else
    vim.notify("Java binary path not configured fully for this platform")
    return nil
  end
end

